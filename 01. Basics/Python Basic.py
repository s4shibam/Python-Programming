print("1.")
print("Hello World!")
print("World is beautiful!")

print("\n2.")
print("Hello World!",   end="")  # , end=""  --> Helps to not to change line.
print("World is beautiful!")

print("\n3.")
print("Hello World!", end=" Wow! ")  # , end=" Wow! "  --> Prints whatever is double quoted.
print("World is beautiful!")

print("\n4.")
print("Hello World!","World is beautiful!")  # , --> Comma between two double quotes, includes space.

print("\n5.")
print("C:\\new delhi")  # \ --> Disable Escape Sequence Characters. Like:- \n, \t, ", '....

print("\n6.")
print("Shibam Saha\nis a pro programmer")  # \n --> New line

print("\n7.")
print("Shibam Saha \tis a pro programmer")  # \t --> Tab
